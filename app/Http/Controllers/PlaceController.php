     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if(isset($_GET['id'])){
            $uuid = $_GET['id'];
            $data['place'] = Place::where('uuid', $uuid)->first();
            $data['questions'] = Question::where('is_active', true)->orderBy('sort', 'asc')->with('answer')->get();
            $data['settings'] = SystemSettings::get(array('corporate_name', 'system_name', 'slogan'));
            return view('rate.rate', ['data'=>$data]);
        }else{
            return 'ID tidak ditemukan';
        }
    }

